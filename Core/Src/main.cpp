void systick_delay_ms(uint32_t ms)
{
  uint32_t index;

  // Ensure 24 bit value
  SysTick->LOAD = 168000 & 0xFFFFFF;

  // Set Clock to Processor clock
  SysTick->CTRL |= (1u<<2);
  // Enable systick
  SysTick->CTRL |= (1u);

  for(index=0; index<ms; index++)
  {
    while(!(SysTick->CTRL &  (1u<<16)))
    {
    }
  }
  // Disable systick
  SysTick->CTRL &= ~(1u);
}

